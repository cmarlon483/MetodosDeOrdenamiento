package Fes.aragon.metodosDeOrdenamiento;

public class CDatos {

    //Atributos

    private int inf,sup;

    //Metodos

    public CDatos(int i, int s){ //Constructor con parametros
        inf= i;
        sup= s;
    }

    public int ObtenerInf(){

        return inf;
    }

    public int ObtenerSup(){

        return sup;
    }

}
